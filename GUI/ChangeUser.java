    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            // TODO add your handling code here:
            String temp;
            String[] array;

            FileReader fileReader = new FileReader(myFile);
            BufferedReader reader = new BufferedReader(fileReader);

            while ((temp = reader.readLine()) != null) {
                array = temp.split("/");
                if (id.getText().equals(array[1]) && pw.getText().equals(array[2])) {
                    JOptionPane.showMessageDialog(null, "인증이 완료되었습니다..");
                    id.setEnabled(false);
                    pw.setEnabled(false);
                    userCheck = true;
                    idpwcheck = true;
                    break;
                }
            }
            reader.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ChangeUser.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ChangeUser.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (idpwcheck == false) {
            JOptionPane.showMessageDialog(null, "인증에 실패했습니다.");
            id.setText("");
            pw.setText("");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (!newpw.getText().equals(newpwCheck.getText())) {
            JOptionPane.showMessageDialog(null, "새 비밀번호와 확인 비밀번호가 다릅니다. 다시 입력해주세요.");
        } else if (userCheck == false) {
            JOptionPane.showMessageDialog(null, "인증이 완료되지 않았습니다. 인증 후 다시 시도해주십시요");
        } else {

            int result = JOptionPane.showConfirmDialog(null, "비밀번호를 변경하시겠습니까??", "주의!", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.CLOSED_OPTION) {
                JOptionPane.showMessageDialog(null, "비밀번호를 변경하지 않았습니다.");
            } else if (result == JOptionPane.YES_OPTION) {
                
                String temp;
                String[] array = new String[4];
                String dummy = "";
                try {

                    BufferedReader reader = new BufferedReader(new InputStreamReader(new FileInputStream(myFile)));

                    while ((temp = reader.readLine()) != null) {
                        array = temp.split("/");
                        if (id.getText().equals(array[1]) && pw.getText().equals(array[2])) {
                            array[2] = newpw.getText();
                            for (int i = 0; i < 5; i++) {
                                dummy += array[i] + "/";
                            }
                            dummy += "\r\n";
                        } else {
                            dummy += (temp + "\r\n");
                        }
                    }

                    FileWriter fw = new FileWriter("User.txt");
                    fw.write(dummy);

                    reader.close();
                    fw.close();
                    JOptionPane.showMessageDialog(null, "비밀번호가 변경되었습니다.");
                    dispose();
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(ChangeUser.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(ChangeUser.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "비밀번호가 변경되지 않았습니다.");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

