        // TODO add your handling code here:
        if (userCheck == false) {
            JOptionPane.showMessageDialog(null, "인증이 완료되지 않았습니다. 인증 후 다시 시도해주십시요");
        } else {
            int result = JOptionPane.showConfirmDialog(null, "회원을 탈퇴하시겠습니까?", "주의!", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.CLOSED_OPTION) {
                JOptionPane.showMessageDialog(null, "회원 탈퇴를 하지 않았습니다.");
            } else if (result == JOptionPane.YES_OPTION) {
                
                String temp;
                String[] array = new String[4];
                String dummy = "";
                
                try {

                    BufferedReader reader = new BufferedReader(new InputStreamReader(new FileInputStream(myFile)));

                    while ((temp = reader.readLine()) != null) {
                        array = temp.split("/");
                        if (!id.getText().equals(array[1]) && !pw.getText().equals(array[2])) {
                            dummy += (temp + "\r\n");
                        }
                    }

                    FileWriter fw = new FileWriter("User.txt");
                    fw.write(dummy);

                    reader.close();
                    fw.close();
                    JOptionPane.showMessageDialog(null, "회원 탈되가 완료되었습니다.");
                    dispose();
                } catch (IOException ex) {
                    Logger.getLogger(ChangeUser.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "회원 탈퇴를 하지 않았습니다.");
            }
        }
