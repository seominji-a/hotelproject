    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here
        
        
        if(id.getText().length() == 0){
            JOptionPane.showMessageDialog(null,("아이디가 입력되지 않았습니다."));
        }
        
        try{
            File myFile = new File("User.txt");
            FileReader fileReader = new FileReader(myFile);
            BufferedReader reader = new BufferedReader(fileReader);
           

            String temp ;
            String[] array ;
            boolean check=false;
            
            while((temp = reader.readLine()) != null){
                array = temp.split("/");
                if(id.getText().equals(array[1])){
                    check = true;
                    break;
                } 
            }
            
            if(check == false){
                JOptionPane.showMessageDialog(null, "사용가능한 아이디 입니다.");
                id.setEnabled(false);
                Enabledcheck = true;
            }else{
                JOptionPane.showMessageDialog(null, "사용중인 아이디 입니다. 다른 아이디를 입력해주세요");
                id.setText("");
            }
            
            reader.close();
        }catch (FileNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Membership.class.getName()).log(Level.SEVERE, null, ex);
        }
                
    }//GEN-LAST:event_jButton4ActionPerformed

