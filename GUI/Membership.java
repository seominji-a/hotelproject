    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(name.getText().length() == 0){
            JOptionPane.showMessageDialog(null,("이름이 입력되지 않았습니다."));
        }else if(id.getText().length() == 0){
            JOptionPane.showMessageDialog(null,("아이디가 입력되지 않았습니다."));
        }else if(Enabledcheck == false){
            JOptionPane.showMessageDialog(null,("중복확인이 되지 않았습니다.."));
        }else if(pw.getText().length() == 0 || checkpw.getText().length() == 0){
            JOptionPane.showMessageDialog(null,("비밀번호 또는 비밀번호 확인이 입력되지 않았습니다."));
        }else if(checkpw.getText().equals(pw.getText())==false){
            JOptionPane.showMessageDialog(null,("비밀번호 또는 비밀번호 확인의 입력이 다릅니다."));
        }else if(men.isSelected() == false && woman.isSelected() == false){
            JOptionPane.showMessageDialog(null,("성별이 선택되지 않았습니다."));
        }else{
            try{
                BufferedWriter bw = new BufferedWriter(new FileWriter("User.txt",true));
                bw.write(name.getText()+"/");
                bw.write(id.getText()+"/");
                bw.write(pw.getText()+"/");
            
                if(men.isSelected() == true){
                    bw.write("남/");
                }else if(woman.isSelected() == true){
                    bw.write("여/");
                }
                bw.write(phone.getText()+"/");
                bw.write("\n");
            
                JOptionPane.showMessageDialog(null, "회원가입이 완료되었습니다.");
                bw.close();

            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, "회원가입에 실패했습니다.");
            }
            dispose();
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

